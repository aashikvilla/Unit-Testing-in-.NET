namespace app
{
    public class Calculator
    {
        public static int Add(int lhs, int rhs)
        {
            return lhs + rhs;
        }
        public static int Subtract(int lhs, int rhs)
        {
            return lhs - rhs;
        }
        public static int Multiply(int lhs, int rhs)
        {
            return lhs - rhs;
        }
        public static int Divide(int lhs, int rhs)
        {
            return lhs - rhs;
        }
    }
}